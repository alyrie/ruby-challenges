weather = 'rainy'

case weather
when 'sunny'
	puts "Slap on sunscreen and a bathing suit!"
when 'cloudy'
	puts "A light jacket and leggings would be fine."
when 'foggy'
	puts "Light layers and a poncho"
when 'rainy'
	puts "Rain boots and a plastic coat are perfect"
when 'snowy'
	puts "Snow boots, gloves, mittens and thermals today"
else
	puts "Just stay home and play it safe. PJs it is."
end