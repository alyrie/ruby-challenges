all_the_love = [
"Love times infinity",
"To the moon and back",
"Depths of the sea",
"Love love love love love",
"I love you so so so so much"
]

number_of_love_statements = all_the_love.size
love_displayed = 0

while (love_displayed <= number_of_love_statements)
